    /**
     * Creates a random maze in a 2D array.
     * 
     * @see <a href="http://en.wikipedia.org/wiki/Maze_generation_algorithm#Recursive_division_method">Recursive_division_method</a>
     * @return 2D array containing a maze
     */
    private static char[][] makeMaze()
    {
        char level[][] = createBlankLevel();
        
        makeMazeRecursive(level, 1, 1, LEVEL_WIDTH-2, LEVEL_HEIGHT-2); //TODO: may need to change but probably not.
        
        return level;
    }

    /**
     * Creates an empty level of standard level height and width. Level will be
     * blank but bordered with wall characters.
     * 
     * @return  2D array containing a maze
     */
    private static char[][] createBlankLevel()
    {   
        char level[][] = new char[LEVEL_HEIGHT][LEVEL_WIDTH];
        
        //reset level to be entirely blank
        for (int y = 0; y < LEVEL_HEIGHT; y++) 
            for (int x = 0; x < LEVEL_WIDTH; x++)
                level[y][x] = ' ';
    
        //top barrier   
        for (int x = 0; x < LEVEL_WIDTH; x++)
            level[0][x] = ICON_WALL;
    
        //bottom barrier
        for (int x = 0; x < LEVEL_WIDTH; x++)
            level[LEVEL_HEIGHT-1][x] = ICON_WALL;
    
        //left barrier
        for (int y = 0; y < LEVEL_HEIGHT; y++)
            level[y][0] = ICON_WALL;
    
        //left barrier
        for (int y = 0; y < LEVEL_HEIGHT; y++)
            level[y][LEVEL_WIDTH-1] = ICON_WALL;
            
        return level;
    }

    /**
     * Creates random horizontal and vertical walls using recursion to create a maze.
     * 
     * @param level  a 2D array of characters
     * @param startX  starting x position
     * @param startY  starting y position
     * @param endX  ending x position
     * @param endY  ending y position
     * @return will return nothing if chamber is less than a 3X3
     */
    private static void makeMazeRecursive(char[][]level, int startX, int startY, int endX, int endY)
    {
        // Find the width and length of a chamber
		int width = endX - startX + 1;
        int length = endY - startY + 1;
      
        // If the width or length of the chamber is less than or equal to 2, do nothing
        if(width <= 2 || length <= 2)
        {
            return;
        }
        // Else recursively create a new and smaller chamber
        else
        {
            // Randomly find new placement for the walls
       		int x = randBetween(startX + 1, endX);
            int y = randBetween(startY + 1, endY);
            int holes;
          
            // Create Horizontal Walls
    	  	for (int a = startX; a <= endX; a++)
    	    	level[y][a] = ICON_WALL;
      
            // Create Vertical Walls
    	    for (int b = startY; b <= endY; b++)
    	        level[b][x] = ICON_WALL;
          
            // Create Holes for Horizontal Walls
            for (int i = 0; i < 3 ; i++)
            {
                holes = randBetween(startX, endX);
                level[y][holes] = ICON_BLANK;
            }
          
            // Create Holes for Vertical Walls
            for (int j = 0; j < 3 ; j++)
            {
                holes = randBetween(startY, endY);
                level[holes][x] = ICON_BLANK;
            }
            
            // Recursive calls to makeMazeRecursive method until base case is met
            makeMazeRecursive(level, startX, startY, x-1, y-1);	// Upper Left Chamber

          	makeMazeRecursive(level, x+1, startY, endX, y-1);	// Upper Right Chamber
          
            makeMazeRecursive(level, startX, y+1, x-1, endY);	// Lower Left Chamber
            
            makeMazeRecursive(level, x+1, y+1, endX, endY);     // Lower Right Chamber
        }
    }

    /**
     * Takes 2 integer parameters and returns a random number between the two.
     * 
     * @param l is an integer
     * @param u is an integer
     */
    private static int randBetween(int l, int u)
    {
        Random randomNumber = new Random();
		int r = randomNumber.nextInt(u - l) + l;

        return r;
    }
    
